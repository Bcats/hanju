package cn.bcat.hanju.pojo.web;

public class IClass {
    private int v_id;
    private String v_name;
    private String class_name;
    private String actor;
    private String imgurl;
    private String status;
    private String download_imgurl;

    public int getV_id() {
        return v_id;
    }

    public void setV_id(int v_id) {
        this.v_id = v_id;
    }

    public String getV_name() {
        return v_name;
    }

    public void setV_name(String v_name) {
        this.v_name = v_name;
    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public String getActor() {
        return actor;
    }

    public void setActor(String actor) {
        this.actor = actor;
    }

    public String getImgurl() {
        return imgurl;
    }

    public void setImgurl(String imgurl) {
        this.imgurl = imgurl;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDownload_imgurl() {
        return download_imgurl;
    }

    public void setDownload_imgurl(String download_imgurl) {
        this.download_imgurl = download_imgurl;
    }
}
